# Приоритеты арифметических операций
a = 68
b = 2
c = 50
d = 43
result = a + b - c + d # Вычисление
print("Результат вычисления: ", a, " + ", b, " - ", c, " + ", d, " = ", result)

# Приоритеты арифметических операций со скобками
a = 30
b = 6
c = 13
d = 9
result = a + b * (c - d) # Вычисление
# Приоритеты: 3 2 1. Решение: 30 + 6 * (13 - 9) = 30 + 6 * 4 = 30 + 24 = 54
print("Результат вычисления: ", a, " + ", b, " * ", c, " - ", d, " = ", result)
 