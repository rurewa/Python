# Задачка на вычисление стоимости авторучек
# Нужно вычислить сумму 15 авторучек, если известна цена каждой - 15 руб.
n = 5; # количество авторучек
price = 15; # Стоимость 1-й авторучки
result = n * price
# Вывод результата на экран
print(n, " авторучек стоят ", result, " рублей")
