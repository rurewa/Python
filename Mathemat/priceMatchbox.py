# Есть 6 коробков спичек. Вычислить стоимоть каждого коробка спичек, 
# если известно, что общая стоимость 6-и коробков спичек равно 36 руб.
n = 6 # Количество коробков
sum = 36 # Общая стоимость 6-и коробков
result = sum / n # Вычисление
# Вывод результата на экран
print("Стоимоть 1-го коробка спичек равна: ", result, " рублей")